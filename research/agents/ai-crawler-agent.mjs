import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { log, ensureDir } from "./lib/shared.mjs";
import { appendToReport } from "./lib/report.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const AGENTS_DIR = path.resolve(__dirname);
const ANALYTICS_FILE = path.join(AGENTS_DIR, "analytics-data.json");
const BLOB_JSONL_KEY = "bot-traffic.jsonl";

function loadAnalytics() {
  try { return JSON.parse(fs.readFileSync(ANALYTICS_FILE, "utf-8")); }
  catch { return { aiCrawlerData: { bots: {}, totalHits: 0, uniqueBots: 0 } }; }
}

function saveAnalytics(data) {
  fs.writeFileSync(ANALYTICS_FILE, JSON.stringify(data, null, 2), "utf-8");
}

async function fetchBlobData() {
  const token = process.env.BLOB_READ_WRITE_TOKEN;
  if (!token) {
    log("[AI Crawler] No BLOB_READ_WRITE_TOKEN — skipping blob fetch");
    return [];
  }
  try {
    const res = await fetch(
      `https://blob.vercel-storage.com/${BLOB_JSONL_KEY}`,
      { headers: { Authorization: `Bearer ${token}` } }
    );
    if (!res.ok) {
      log(`[AI Crawler] Blob fetch: HTTP ${res.status}`);
      return [];
    }
    const text = await res.text();
    return text.trim().split("\n").filter(Boolean).map(line => {
      try { return JSON.parse(line); }
      catch { return null; }
    }).filter(Boolean);
  } catch (err) {
    log(`[AI Crawler] Blob fetch failed: ${err.message}`);
    return [];
  }
}

export async function runAiCrawlerAnalysis() {
  log("[AI Crawler Agent] Starting AI crawler analysis...");
  const analytics = loadAnalytics();

  const records = await fetchBlobData();
  if (records.length === 0) {
    log("[AI Crawler Agent] No bot records found yet.");
    analytics.aiCrawlerData = analytics.aiCrawlerData || { bots: {}, totalHits: 0, uniqueBots: 0, firstSeen: null, lastSeen: null };
    analytics.lastCrawlerRun = new Date().toISOString();
    saveAnalytics(analytics);
    return analytics.aiCrawlerData;
  }

  const bots = {};
  const botHits = {};
  const urlCounts = {};
  let totalBotHits = 0;
  let totalHumanHits = 0;
  let firstSeen = null;
  let lastSeen = null;

  for (const r of records) {
    if (r.timestamp) {
      if (!firstSeen || r.timestamp < firstSeen) firstSeen = r.timestamp;
      if (!lastSeen || r.timestamp > lastSeen) lastSeen = r.timestamp;
    }

    const botName = r.bot || (r.userAgent && r.userAgent.includes('bot') ? 'UnknownBot' : null);

    if (botName) {
      totalBotHits++;
      if (!bots[botName]) bots[botName] = { hits: 0, firstSeen: r.timestamp, lastSeen: r.timestamp, paths: {} };
      bots[botName].hits++;
      if (r.timestamp) {
        if (r.timestamp < bots[botName].firstSeen) bots[botName].firstSeen = r.timestamp;
        if (r.timestamp > bots[botName].lastSeen) bots[botName].lastSeen = r.timestamp;
      }
      const path = r.path || '/';
      bots[botName].paths[path] = (bots[botName].paths[path] || 0) + 1;
      urlCounts[path] = (urlCounts[path] || 0) + 1;
      botHits[botName] = (botHits[botName] || 0) + 1;
    } else {
      totalHumanHits++;
    }
  }

  const sortedBots = Object.entries(bots)
    .sort((a, b) => b[1].hits - a[1].hits)
    .map(([name, data]) => ({
      name,
      hits: data.hits,
      firstSeen: data.firstSeen,
      lastSeen: data.lastSeen,
      topPaths: Object.entries(data.paths).sort((a, b) => b[1] - a[1]).slice(0, 5).map(([p, c]) => `${p} (${c}x)`),
    }));

  const topUrls = Object.entries(urlCounts).sort((a, b) => b[1] - a[1]).slice(0, 10);

  const result = {
    bots: sortedBots,
    totalBotHits,
    totalHumanHits,
    uniqueBots: sortedBots.length,
    topUrls,
    firstSeen,
    lastSeen,
    analyzedAt: new Date().toISOString(),
  };

  analytics.aiCrawlerData = result;
  analytics.lastCrawlerRun = new Date().toISOString();
  saveAnalytics(analytics);

  // Generate report
  const reportLines = [
    "## AI Crawler Report",
    `**Analysis date:** ${result.analyzedAt}`,
    `**Period:** ${result.firstSeen || "N/A"} → ${result.lastSeen || "N/A"}`,
    "",
    `**Total bot hits:** ${result.totalBotHits}`,
    `**Total human hits:** ${result.totalHumanHits}`,
    `**Unique bots detected:** ${result.uniqueBots}`,
    "",
    "### Bot Breakdown",
    "| Bot | Hits | First Seen | Last Seen | Top Paths |",
    "|---|---|---|---|---|",
  ];

  for (const b of sortedBots) {
    reportLines.push(`| ${b.name} | ${b.hits} | ${b.firstSeen || "N/A"} | ${b.lastSeen || "N/A"} | ${b.topPaths.join(", ")} |`);
  }

  reportLines.push("");
  reportLines.push("### Most Visited URLs by Crawlers");
  for (const [url, count] of topUrls) {
    reportLines.push(`- ${url}: ${count}x`);
  }

  reportLines.push("");
  reportLines.push("*Auto-generated by AI Crawler Agent*");

  appendToReport("AI Crawler Agent", reportLines.join("\n"));

  log(`[AI Crawler Agent] Complete. ${result.totalBotHits} bot hits from ${result.uniqueBots} unique bots.`);
  return result;
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  runAiCrawlerAnalysis().catch(console.error);
}
